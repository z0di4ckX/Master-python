"""
Ejercicio 4. Pedir dos numeros al usario y hacer todas las 
operaciones basicas de una calculadora y mostrarla por pantalla.
"""

numero1 = int(input("Introduce el primero numero: "))
numero2 = int(input("Introduce el segundo numero: "))

print("###### CALCULADORA ###########")
print("Suma: " + str(numero1+numero2))
print("Resta: " + str(numero1-numero2))
print("Multiplicacion: " + str(numero1*numero2))
print("Division: " + str(numero1/numero2))
